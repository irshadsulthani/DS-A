/**
 * ! Tree 
 * * A tree is a non-linear data structure.
 * * It is a collection of nodes connected by edges.
 * * Represents hierarchical relationships between elements.
 * ? Example: Family tree, file directory system, organizational chart.
 * 
 * ! Features of a Tree:
 * 
 * * Root Node:
 * ? The topmost node in a tree.
 * ? Every tree has only one root node.
 * 
 * * Parent and Child Nodes:
 * ? A node that has children is called a parent.
 * ? The nodes directly connected below a node are called children.
 *  
 * * Edge:
 * ? A connection between two nodes in a tree.
 * 
 * * Leaf Node:
 * ? A node that has no children.
 * 
 * * Subtree:
 * ? Any node along with its descendants forms a subtree.
 * 
 * * Height of a Tree:
 * ? The length of the longest path from the root node to a leaf.
 * 
 * * Depth of a Node:
 * ? The number of edges from the root node to that node.
 * 
 * * Binary Tree:
 * ? Each node has at most two children.
 * ? Referred to as the left child and right child.
 * 
 * ! Applications of Trees:
 * 
 * * Binary Search Tree (Used for searching and sorting data efficiently.) 
 * * File System (Represents hierarchical file structure.)
 * * Network Routing (Represents network hierarchies.)
 * * Tries (Used for efficient storage and retrieval of strings.)
 * 
 * ! Types of Trees:
 * 
 * * General Tree: A tree where each node can have any number of children.
 * * Binary Tree: Each node has at most two children (left and right).
 * * Binary Search Tree (BST): A binary tree where the left child is smaller and the right child is greater.
 * * AVL Tree: A self-balancing binary search tree.
 * * Red-Black Tree: A self-balancing BST with an extra color property.
 * * B-Tree: A self-balancing tree used in databases and file systems.
 * * Heap: A tree-based data structure used for priority queues.
 * * Trie (Prefix Tree): Used for storing strings efficiently.
 * * N-ary Tree: A tree where each node can have at most N children.
 */
